//  Find the Character Is Vowel or Not ? 

function CheckVowel(letter){
    if(letter == 'a' ||letter == 'e' || letter == 'i' || letter == 'o' || letter == 'u' ){
        console.log("letter is vowel");
    }
    else{
        console.log("letter is not vowel");
    }
}

CheckVowel('t');
CheckVowel('i');